            for toggle in [s.toggle_on, s.toggle_off] {
                assert_fits(
                    &s.panel_errors.replacen("{}", toggle, 1),
                    &format!("{nm}.panel_errors+'{toggle}'"),
                );
            }

            // ── panel_count — worst case two-digit number ────────────────────
            assert_fits(
                &s.panel_count.replacen("{}", "81", 1),
                &format!("{nm}.panel_count"),
            );

